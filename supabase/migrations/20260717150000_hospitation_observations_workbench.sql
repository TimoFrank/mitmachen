drop policy if exists "hospitation observations authenticated read active" on public.hospitation_observations;
create policy "hospitation observations authenticated read active"
on public.hospitation_observations for select to authenticated
using (
  (status <> 'archived' and exists (
    select 1 from public.hospitations h
    where h.id = hospitation_id and h.status <> 'Archiviert'
  ))
  or public.current_profile_role() = 'admin'
);

drop policy if exists "hospitation observations editor admin insert" on public.hospitation_observations;
create policy "hospitation observations editor admin insert"
on public.hospitation_observations for insert to authenticated
with check (public.current_profile_role() in ('editor', 'admin'));

drop policy if exists "hospitation observations editor admin update" on public.hospitation_observations;
create policy "hospitation observations editor admin update"
on public.hospitation_observations for update to authenticated
using (public.current_profile_role() in ('editor', 'admin'))
with check (public.current_profile_role() in ('editor', 'admin'));

drop policy if exists "hospitation observation changes authenticated read" on public.hospitation_observation_changes;
create policy "hospitation observation changes authenticated read"
on public.hospitation_observation_changes for select to authenticated
using (exists (
  select 1 from public.hospitation_observations o where o.id = observation_id
));

